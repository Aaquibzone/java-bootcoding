package oops.objectparameter.pen;

public class Pen {
    int price;
    String penCompany;

    String penType;

    String penInk;

    public int getPrice() {
        return price;
    }

    public void setPrice(int price) {
        this.price = price;
    }

    public String getPenCompany() {
        return penCompany;
    }

    public void setPenCompany(String penCompany) {
        this.penCompany = penCompany;
    }

    public String getPenType() {
        return penType;
    }

    public void setPenType(String penType) {
        this.penType = penType;
    }

    public String getPenInk() {
        return penInk;
    }

    public void setPenInk(String penInk) {
        this.penInk = penInk;
    }
}
